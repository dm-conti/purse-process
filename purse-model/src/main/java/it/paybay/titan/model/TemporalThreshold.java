package it.paybay.titan.model;

public class TemporalThreshold {
	private String maxThreshold; 
 	private String defaultThreshold;
	
 	public String getMaxThreshold() {
		return maxThreshold;
	}
	public void setMaxThreshold(String maxThreshold) {
		this.maxThreshold = maxThreshold;
	}
	public String getDefaultThreshold() {
		return defaultThreshold;
	}
	public void setDefaultThreshold(String defaultThreshold) {
		this.defaultThreshold = defaultThreshold;
	}
}